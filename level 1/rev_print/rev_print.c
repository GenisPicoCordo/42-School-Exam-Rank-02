#include <unistd.h>

int	main(int argc, char **argv)
{
	int	l;

	l = 0;
	if (argc == 2)
	{
		while (argv[1][l])
			l++;
		while (0 < l)
		{
			l--;
			write(1, &argv[1][l], 1);
		}
	}
	write(1, "\n", 1);
}
